#!/usr/bin/env python3
"""Тест для проверки преобразования четырехзначных чисел."""

from text_preprocessor import TextPreprocessor

# Инициализация препроцессора
preprocessor = TextPreprocessor()

# Тестовые примеры
test_cases = [
    ("В 1955 году", "В одна тысяча девятьсот пятьдесят пятый году"),
    ("1955 год", "одна тысяча девятьсот пятьдесят пятый год"),
    ("2024 год", "две тысячи двадцать четвертый год"),
    ("1812 год", "одна тысяча восемьсот двенадцатый год"),
    ("1955", "одна тысяча девятьсот пятьдесят пять"),
    ("2000", "две тысячи"),
    ("1999", "одна тысяча девятьсот девяносто девять"),
    ("В 1955 году родился", "В одна тысяча девятьсот пятьдесят пятый году родился"),
    ("123", "сто двадцать три"),
    ("5", "пять"),
]

print("Тестирование преобразования чисел:\n")
print("=" * 80)

for input_text, expected in test_cases:
    result = preprocessor.replace_numbers_with_words(input_text)
    status = "✓" if expected.lower() in result.lower() else "✗"
    print(f"{status} Вход: {input_text}")
    print(f"  Результат: {result}")
    print(f"  Ожидание: {expected}")
    print()

print("=" * 80)
print("Тестирование завершено!")
